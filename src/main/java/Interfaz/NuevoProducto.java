    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String nombre = jTextField2.getText();
        float precio = Float.parseFloat(jTextField3.getText());
        String fechaElaboracion = jTextField4.getText();
        String fechaVencimineto = jTextField5.getText();
        
        nuevoProducto.setNombre(nombre);
        nuevoProducto.setPrecio(precio);
        nuevoProducto.setFechaElaboracion(fechaElaboracion);
        nuevoProducto.setFechaVencimiento(fechaVencimineto);
        
        if(onProductoModificado != null){
            onProductoModificado.run(); // aqui se ejecuta mi lambda osea mi codigo anterior
        }
        
        this.dispose();
    }//GEN-LAST:event_jButton3ActionPerformed

